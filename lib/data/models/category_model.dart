@freezed
abstract class CategorynModel with _$CategorynModel {
  const CategorynModel._();

  @jsonSerializable
  const factory CategorynModel({
    required String id,
    required String english,
    required String arabic,
    required String arabicPronunciation,
    required String englishPronunciation,
    required CategoryEnum category,
    // Id get isarId => fastHash(id!);
  }) = _CategorynModel;

  factory CategorynModel.fromJson(Map<String, dynamic> json) => _$CategorynModelFromJson(json);
}

@JsonEnum(alwaysCreate: true)
enum CategoryEnum {
  @JsonValue('animals')
  animals,
  @JsonValue('colors')
  colors,
  @JsonValue('numbers')
  numbers,
  @JsonValue('family')
  family,
  @JsonValue('school')
  school,
  @JsonValue('food')
  food;

  String toJson() => _$CategoryEnumEnumMap[this]!;

  factory CategoryEnum.fromJson(String json) {
    return CategoryEnum.values.firstWhere(
      (e) => e.toJson() == json,
      orElse: () => CategoryEnum.animals,
    );
  }
}

